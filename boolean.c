// boolean in c
// boolean is a data type that can have only two possible values, true or false.
//lets use real world example to understand boolean data type.
#include<stdio.h>
#include<stdbool.h>
int main() {
    bool isLightOn = true;
    bool isDoorOpen = false;
    printf("isLightOn: %d\n", isLightOn);
    printf("isDoorOpen: %d\n", isDoorOpen);
    return 0;
}